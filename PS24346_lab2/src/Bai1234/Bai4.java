    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String user = txtUsername.getText();
        String pass = txtPassword.getText();
        String role = "";
        if (rdoAdmin.isSelected()) {
            role = "admin";
        } else {
            role = "user";
        }
        User u = new User(user, pass, role);
        list.add(u);
        fillTable();
        clear();
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblUserListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserListMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            tabs.setSelectedIndex(0);
            int r = tblUserList.getSelectedRow();
            if (r < 0) {
                return;
            }
            txtUsername.setText(tblUserList.getValueAt(r, 0).toString());
            txtPassword.setText(tblUserList.getValueAt(r, 1).toString());
            String role = tblUserList.getValueAt(r, 2).toString();
            if (role.equals("admin")) {
                rdoAdmin.setSelected(true);
            } else {
                rdoUser.setSelected(true);
            }
    }//GEN-LAST:event_tblUserListMouseClicked
    }

