    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblUserList.getModel();
        model.setRowCount(0);
        for (User u : list) {
            Object[] row = {u.getUsername(), u.getPassword(), u.getRole()};
            model.addRow(row);
        }
    }

